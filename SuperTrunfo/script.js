var cartaMaquina;
var cartaJogador;

function sortearCarta() {
  var indexCartaMaquina = parseInt(Math.random() * 24);
  cartaMaquina = cartas[indexCartaMaquina];

  var indexCartaJogador = parseInt(Math.random() * 24);
  while (indexCartaMaquina == indexCartaJogador) {
    var indexCartaJogador = parseInt(Math.random() * 24);
  }
  cartaJogador = cartas[indexCartaJogador];

  document.getElementById("btnSortear").disabled = true;
  document.getElementById("btnJogar").disabled = false;

  exibirCarta(cartaJogador, "carta-jogador");
}

function exibirCarta(carta, id) {
  var cartaExibida = document.getElementById(id);
  cartaExibida.style.backgroundImage = `url(${carta.imagem})`;
  var moldura = '<img src="https://www.alura.com.br/assets/img/imersoes/dev-2021/card-super-trunfo-transparent-ajustado.png" style=" width: inherit; height: inherit; position: absolute;">';
  var tagHTML = "<div id='opcoes' class='carta-status'>";
  
  var opcoesTexto = "";
  for (var atributo in cartaJogador.atributos) {
    opcoesTexto +=
      "<br><input type='radio' name='atributo' value='" + atributo + "'/>" + atributo + " " + carta.atributos[atributo];
  }
  var nome = `<p class="carta-subtitle">${carta.nome}</p>`
  
  cartaExibida.innerHTML = moldura + nome + tagHTML + opcoesTexto + "</div>";
  
}

function obtemAtributoSelecionado() {
  var radioAtributos = document.getElementsByName("atributo");

  for (var i = 0; i < radioAtributos.length; i++) {
    if (radioAtributos[i].checked) {
      return radioAtributos[i].value;
    }
  }
}

function jogar() {
  var atributoSelecionado = obtemAtributoSelecionado();
  var elementoResultado = document.getElementById("resultado");
  var valorCartaJogador = cartaJogador.atributos[atributoSelecionado];
  var valorCartaMaquina = cartaMaquina.atributos[atributoSelecionado];

  exibirCarta(cartaMaquina, "carta-maquina");

  if (valorCartaJogador > valorCartaMaquina) {
    htmlResultado = "<p class='resultado-final'>Você venceu!, o atributo da carta escolhido, é maior que o da máquina.</p>";
  } else if (valorCartaMaquina > valorCartaJogador) {
    htmlResultado = "<p class='resultado-final'>Você perdeu, o atributo da carta escolhido, é menor que o da máquina.</p>";
  } else {
    htmlResultado = "<p class='resultado-final'>Empatou</p>";
  }
  elementoResultado.innerHTML = htmlResultado;
  
  document.getElementById("btnJogar").disabled = true;
}